public class Swap2Num {
    public static void main(String[] args) {
        String s1 = "World!. ";
        String s2 = "Hello ";
        // we want to swap this String

        String temp = s1;
        s1 = s2;
        s2 = temp;
//        System.out.println(s1.matches(s2));

        System.out.println("s1 is "+ s1 );
        System.out.println("s2 is "+s2);

    }
}
